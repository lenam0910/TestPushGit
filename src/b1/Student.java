public class Student {
    private int id;
    private int Name;

    public Student() {
    }

    public Student(int id, int Name) {
        this.id = id;
        this.Name = Name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getName() {
        return Name;
    }

    public void setName(int Name) {
        this.Name = Name;
    }
    
    
}
